	HAL_Delay(2000);
	HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_11);
	HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_5);
	HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_1);
	HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_7);

